package org.example.section1.inheritance;

public class Inheritance {
    /**Inheritance is the oops concept used consist of parent class and one or more child class in which
     child class will acquire all the characteristics of parent
     It can be implemented using extends keyword */
    public static void main(String[] args) {
        Dog dog=new Dog();
        //dog object gets the output from eat() method which is present in Animal class
        dog.eat();

    }
}
